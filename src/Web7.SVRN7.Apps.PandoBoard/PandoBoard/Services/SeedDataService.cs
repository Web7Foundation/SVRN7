    private static Contact CreateContact(string name, string didKey, string hex, ContactStatus status, double velocity)
    {
        var contact = new Contact
        {
            Name          = name,
            Did           = $"did:drn:svrn7.net:{didKey}",
            Color         = SKColor.Parse(hex),
            Status        = status,
            VelocityScore = velocity
        };

        var conversations = GetConversations(didKey);

        foreach (var (outMsg, inMsgs) in conversations)
        {
            var thid = Guid.NewGuid().ToString();
            var thread = new ConversationThread { Thid = thid };
            var baseMinutes = 120 + contact.Threads.Count * 15;

            thread.Messages.Add(Msg(thid, outMsg, true, null, minutesAgo: baseMinutes));
            foreach (var (inMsg, offset) in inMsgs.Select((m, i) => (m, i)))
            {
                // thread index = offset + 1; every 3rd (index 2, 5, 8, …) is incoming
                bool outbound = (offset + 2) % 3 != 0;
                thread.Messages.Add(Msg(thid, inMsg, outbound, null, minutesAgo: baseMinutes - (offset + 1) * 2, verified: true));
            }

            // Every 3rd conversation gets a short joyful outbound acknowledgement
            if (contact.Threads.Count % 3 == 2)
            {
                string ack = JoyfulAcks[(contact.Threads.Count / 3) % JoyfulAcks.Length];
                thread.Messages.Add(Msg(thid, ack, true, null, minutesAgo: baseMinutes - (inMsgs.Length + 1) * 2));
            }

            if (contact.Threads.Count < 3)
                thread.UnreadCount = 1;

            contact.Threads.Add(thread);
        }

        return contact;
    }

